#include "3-calc.h"

/**
 * op_add - returns the sum of a and b
 * @a: first num
 * @b: second num
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two int nums
 * @a: first num.
 * @b: second num.
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two int nums
 * @a: first num
 * @b: second num
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two nums
 * @a: first num
 * @b: second num
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of the division of two int nums
 * @a: first num
 * @b: second num
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
